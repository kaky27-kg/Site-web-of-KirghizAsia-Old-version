<p>
<br><b style="color: #E25A00;">Jour 1: Vol </b>
<br>Vol Kirghizistan 

<br><br><b style="color: #E25A00;">Jour 2: Bichkek. </b>
<br>Accueil à l’aéroport de Manas et transfert à Bichkek. Repos chez l’habitant. Visite de Bichkek, ville contrastée, à l’architecture soviétique et aux grands espaces verts. Vous visiterez le Grand Bazar Osh, ses allées animées, ses marchandises artisanales, ses contrastes culturels, entre spécialités chinoises et fruits secs du Moyen Orient. 
<br><i>Transfert : 45 min; Hébergement : Chez l'habitant</i>

<br><br><b style="color: #E25A00;">Jour 3: Soussamir </b>
<br>Route pour la vallée de Soussamir. Nous allons longer la vallée de Chui ensuite passer le col de Too-Ashou spectaculaire et descendre dans la vallée de Soussamir, 
un des plus grands jaïloos du Kirghizistan. 
<br><i>Transfert :  4 h; Hébergement : Tente</i>

<br><br><b style="color: #E25A00;">Jour 4: Randonnée Jaïzan</b>
<br>Premier jour de randonnée itinérante de 5 jours sur le massif central de Tian-Shan.  Nous marcherons jusqu’au pied du col de Jaïsan. Nous installerons notre campements autours de plusieurs petits lacs glaciaires où vous pouvez voir des anciens pétroglyphes, dessins sur des pierres. Les chevaux de bas portent les bagages et la cuisine.
<br><i> Heure de marche : 4/5 h; Dénivelé :+600m; Hébergement : Tente </i>

<br><br><b style="color: #E25A00;">Jour 5: Randonnée Ters</b>
<br>Marche pour atteindre le col de Jaïsan à 3500 m d'altitude, ensuite nous descendrons dans la vallée de Ters. Possibilité de voir des animaux sauvages comme bouquetin etc. 
<br><i>Heure de marche : 5/6 h; Dénivelé :+300m / -900m; Hébergement : Yourte</i>

<br><br><b style="color: #E25A00;">Jours 6: Randonnée Kichi-Japirtmakty</b>
<br>Le col et le lac de Boru-Korboz est au programme de cette journée. Sur le chemin des marmottent se font entendre de loin. Pic-nique au bord du lac glaciaire, ensuite le passage du col de 3400 m d'altitude et descente vers le jaïloo de Kichi-Japrtmakty. 
<br><i> Heure de marche : 4/5 h; Dénivelé :+800m / -600M; Hébergement : Tente </i>

<br><br><b style="color: #E25A00;">Jours 7: Randonnée Jylaan Bazar</b>
<br>Nous quittons cet endroit paisible et allons faire notre dernier col pour cette étape de randonnée. En arrivant au col, une vue splendide sur toute la vallée de Ketmen-Tobo.  
<br> <i> Heure de marche : 4/5 h; Dénivelé :+600m / -1200m; Hébergement : Tente </i>

<br><br><b style="color: #E25A00;">Jour 8: Randonnée Toktogoul</b>
<br>Nous descendons vers le village de Toktogoul. La maitresse de la famille chez qui vous serez hébergé, est le professeur de français à l'école du village et le responsable d'une ONG qui développe l'artisanat traditionnel kirghiz.
<br> <i> Heure de marche : 4/5 h; Dénivelé :+300m / -900m; Hébergement : Chez l’habitant </i>

<br><br><b style="color: #E25A00;">Jour 9: Toktogoul </b>
<br>Journée pour décourvir l’artisanat traditionnel Kirghize avec ONG Jash-Muun. La fabrication du feutre et des tapis kirghiz  « Chirdak ».  
L'après-midi, repos et baignade au lac de Toktogoul. Possibilité d'y aller à velo. 
<br> <i> Hébergement : Chez l’habitant </i> 

<br><br><b style="color: #E25A00;">Jours 10: Transfert à Song-Kol </b>
<br>Route pour le lac Song-Kol, situé au cœur des Monts Célestes à 3015 m d'altitude. Durant le trajet
nous longerons la rivière de Kokomerene  qui coulent dans un splendide canyon aux couleurs ocres rouges. Après le passage du col Chil-Bel à 3300 m d'altitude un  paysage époustouflant se présente à vous.
<br> <i> Transfert :  6 h; Hébergement : Sous tente </i>

<br><br><b style="color: #E25A00;">Jour 11: Randonnée Song-Kol</b>
<br>Randonnée d'une journée sur l'alpage  vallonné avec une vue grandiose sur le lac Song-Kol. 
On part de notre campement de Toulpar Tash par la rive nord du lac et rejoind notre famille d'accueil nomade de Tourousbek.  
<br> <i> Heure de marche : 4/5 h; Dénivelé : +200m; Hébergement : Sous la yourte </i>

<br><br><b style="color: #E25A00;">Jour 12: Song-Kol </b>
<br>Journée libre à Song-Kol au bord du lac pour partager la vie des familles nomades. Possibilité de faire une balade à cheval pour les cavaliers débutants. 
<br> <i> Hébergement : Sous la yourte </i> 

<br><br><b style="color: #E25A00;">Jour 13: Randonnée Kizart, puis transfer pour Issyk-Kul </b>
<br>Randonnée pour descendre au village de Kizart en passant par le col de Tuz-Achou.  Déjeuner chez l'habitant. 
Après-midi nous prenons la route en passant par Kochkor pour le village d'Echperov situé au rive sud du lac Issik-Kul.
<br> <i> Transfert :  3 h; Heure de marche : 4/5 h; Dénivelé :+400m /-800m; Hébergement : Chez l'habitant </i> 

<br><br><b style="color: #E25A00;">Jour 14: Issyk-Kul Canyon de Ak-Saï</b>
<br>Repos au bord du lac Issyk-Kul, un endroit paisible entouré par des canyons rouges et une vue magnifique sur les sommets enneigés de Terskei Ala-Too. Soirée autour du feu. 
<br> <i>Hébergement : Tente </i>

<br></br><b style="color: #E25A00;">Jour 15: Bichkek </b>
</br>Retour à Bishkek. Sur la route nous nous arrêterons à Bourana, minaret datant du royaume des Karakhanides (11 siècle). Soirée d'adieu au restaurant traditionnel. 
</br> <i>Transfert :  4/5 h;  Hébergement : Chez l’habitant </i>

<br></br><b style="color: #E25A00;">Jour 16: Vol de retour </b>
</br>Transfert à l'aéroport. 

</p>


<h2 style="text-align:center";>Fiche Technique </h2>
 
<p>
<b style="color: #E25A00;">Encadrement : </b>un guide kirghiz  francophone, une personne qui s’occupe des chevaux . Durant la randonnée les bagages sont portés par des chevaux.
<br><br><b style="color: #E25A00;">Déplacement sur place :</b> Minibus, voiture
<br><br><b style="color: #E25A00;">Climat : </b> Le climat au Kirghizstan est continental, avec un été chaud ( de 30 à 42°C) et un hiver froid (de -10 à -25°C). 
L'été, en altitude, la température varie entre +10 et +25°C le jour, et entre 0 et +5 °C la nuit. 
<br><br><b style="color: #E25A00;">Matériel nécessaire :</b>
 <br> - 1 petit sac à dos pour les affaires personnelles pour une journée
 <br> - 1 sac avec vos affaires de voyage (un seul par personne) qui nous suit lors de nos déplacements en minibus
 <br> - 1 duvet (confort -5°C) et 1 matelas gonflable.
 <br> - une tenue chaude pour les soirées en alpage, mais aussi une tenue légère (shorts et pantalons) pour les autres moments et une cape pour la pluie
 <br> - Chaussures de rechange, style tennis, ou sandales confortables
 <br> - Gourde de 1 litre
 <br>- Protection contre le soleil : chapeau, bob, lunettes, crèmes...
 <br> - Lampes électriques
 <br> - Affaires de toilette
 <br> - Pochette ventrale pour les papiers et autres effets personnels
 <br> - Pharmacie personnelle : protection contre les ampoules, petits problèmes intestinaux, désinfectant, antalgique. 
 <br><br><b style="color: #E25A00;">  Indispensable :</b> traitement de l'eau (micro pur), traitements personnels éventuels. 
   (L’accompagnateur dispose d’une pharmacie collective pour les soins d’urgence)
 <br><br><b style="color: #E25A00;">Remarque concernant les problèmes de santé et de pharmacie :</b>
Il est de la responsabilité de chacun d’entre vous d’avertir KirghizAsia, avant le départ, de tout problème médical (allergie, antécédent médical, réaction à certains  médicaments, etc.), et de prendre, si besoin est, les médicaments qui vous sont spécifiques.
<br><br><b style="color: #E25A00;">Formalités :</b>
<br>Passeport valable au moins 6 mois après la date du retour + Visa (obtention possible à l'arrivée à l'aéroport de Bichkek).
Aucune vaccination obligatoire. Nous vous recommandons néanmoins d'être à jour dans vos rappels antitétaniques.

</p>
