<p>

<br><b style="color: #E25A00;">Jour 1: Vol pour Bichkek</b>
<br><br><b style="color: #E25A00;">Jour 2: Bichkek </b>
<br>Arrivée tôt le matin à l’aéroport de Manas. Transfert à Bichkek et repos chez l’habitant. Visite de Bichkek, ville contrastée, à l’architecture soviétique et aux grands espaces verts. Vous visiterez le Grand Bazar Osh, ses allées animées, ses marchandises artisanales, ses mélanges culturels, entre spécialités chinoises et fruits secs du Moyen Orient. 
<br>- Transfert : 45 min
<br>- Hébergement : Chez l'habitant

<br><br><b style="color: #E25A00;">Jour 3: Karakol </b>
<br>Transfert à Karakol par la rive nord du lac Issyk- Kul, 5 heures de route. Durant le trajet, visite de la tour de Burana, minaret datant du royaume des Karakhanides (11 siècle).
<br>- Transfert : 5 h
<br>- Hébergement : Chez l'habitant

<br><br><b style="color: #E25A00;">Jour 4: Karakol</b>
<br>Visite de la ville: musée de Prejivalskii, église orthodoxe et mosquée doungane. Après-midi, pic-nique et balade dans les montagnes aux alentours. 
<br>- Heure de marche : 1/2 h
<br>- Hébergement : Chez l'habitant

<br><br><b style="color: #E25A00;">Jour 5: Kizil-Tuu </b>
<br>Dans la matinée transfert à Jeti-Oguz, les canyons rouges appelés « Sept Taureaux ». Petite randonnée dans la vallée des fleurs. Dans l’après-midi nous continuons notre route pour Kizil-Tou, village reconnu pour ses artisans de yourte. Occasion pour découvrir la fabrication artisanale de la yourte. 
<br>- Transfert : 3 heures
<br>- Heure de marche : 2/3 h
<br>- Hébergement : Chez l'habitant

<br><br><b style="color: #E25A00;">Jour 6 : Tash-Rabat </b>
<br>Départ pour Tash-Rabat, ancien caravane sérail datant du 14 siècle, en passant par le village de Kochkor où nous nous arreterons pour une pose de déjeuner. Dans l’après-midi nous continuons la route pour Tash-Rabat.
<br>- Transfert : 5 h
<br>- Hébergement : Sous la yourte

<br><br><b style="color: #E25A00;">Jour 7 : Tash-Rabat </b>
<br>Visite du caravane sérail. Ensuite une balade et rencontre avec des éleveurs de yacks.
<br>- Heure de marche : 2/3 h
<br>- Hébergement : Sous la yourte

<br><br><b style="color: #E25A00;">Jour 8: Song-Kol</b>
<br>Départ pour le lac de Song-Kol, situé au cœur des Monts Célestes (3016 m d’altitude). Arrivée au col de Moldo-Achou à 3400 m d’altitude, une vue époustouflante sur le lac de Song-Kol. 
<br>- Transfert : 5/6 h
<br>- Hébergement : Sous la yourte

<br><br><b style="color: #E25A00;">Jour 9: Song-Kol</b>
<br>Deuxième jour à Song-Kol nous passerons du temps avec la famille pour partager leur vie nomade: fabrication de la crème, traite de jument etc. 
<br>- Hébergement : Sous la yourte

<br><br><b style="color: #E25A00;">Jour 10 : Kazarmane </b>
<br>Étape à Kazarmane, nous prenons la direction vers le Sud du Kirghizistan où la culture est plus sédentaire que nomade. 
<br>- Transfert : 5/6 h
<br>- Hébergement : Chez l’habitant

<br><br><b style="color: #E25A00;">Jour 11 : Osh </b>
Route pour la ville d’Osh, la capitale sud du pays, située dans la vallée du Fergana. La route est praticable durant les trois mois d’été dans l’année.
<br>- Transfert : 5/6 h
<br>- Hébergement : Chez l’habitant

<br><br><b style="color: #E25A00;">Jour 12 : Osh </b>
<br>Visite de la ville d’Osh : le Mont Soulaïman, le bazar d’Osh, musée des beaux arts. 
<br>- Hébergement : Chez l’habitant

<br><br><b style="color: #E25A00;">Jours 13 : Toktogoul </b>
<br>Route pour le village de Toktogoul, sur le trajet visite de la tour de Ozgon. La maitresse de la famille chez qui vous serez hébergé, est le professeur de français à l'école du village et le responsable d'ONG qui développe l'artisanat traditionnel kirghiz. 
<br>- Transfert : 5/6 h
<br>- Hébergement : Chez l’habitant

<br><br><b style="color: #E25A00;">Jours 14 : Toktogoul </b>
<br>Visite de l’atelier artisanal, découverte de fabrication du feutre et des tapis kirghiz. 
<br>- Hébergement : Chez l’habitant

<br><br><b style="color: #E25A00;">Jour 15 : Bichkek </b>
<br>Retour à Bichkek. Le soir diner au restaurant traditionnel. 
<br>- Transfert : 4/5 h
<br>- Hébergement : Chez l’habitant

<br><br><b style="color: #E25A00;">Jour 16 : Vol de retour </b>
<br>Transfert à l'aéroport. 

</p>


<h2 style="text-align:center";>Fiche Technique </h2>
 
<p>

<br><b>Encadrement :</b> un guide kirghiz  francophone
<br><b>Déplacement sur place :</b> Minibus, voiture.
<br><b>Climat : </b>Le climat en Asie Centrale est continental, avec un été chaud ( de 30 à 42°C) et un hiver froid (de -10 à -25°C). En altitude, en soirée et en matinée, températures assez fraîches.
<br><b>Matériel nécessaire :</b>
<br>  - 1 petit sac à dos pour les affaires personnelles pour une journée
<br> - 1 sac avec vos affaires de voyage (un seul par personne) qui nous suit lors de nos déplacements en minibus
<br> - 1 duvet de montagne 
<br> - une tenue chaude pour les soirées en alpage, mais aussi une tenue légère (shorts et pantalons) pour les autres moments et une cape pour la pluie
 <br> - Chaussures de rechange, style tennis, ou sandales confortables
 <br> - Gourde de 1 litre
 <br> - Protection contre le soleil : chapeau, bob, lunettes, crèmes...
 <br> - Lampes électriques
 <br> - Affaires de toilette
  <br>- Pochette ventrale pour les papiers et autres effets personnels
 <br> - Pharmacie personnelle : protection contre les ampoules, petits problèmes intestinaux, désinfectant, antalgique. 
 <br><b>  Indispensable : </b>traitement de l'eau (micro pur), traitements personnels éventuels. 
   (L’accompagnateur dispose d’une pharmacie collective pour les soins d’urgence)
 <br><b>Remarque concernant les problèmes de santé et de pharmacie :</b>
Il est de la responsabilité de chacun d’entre vous d’avertir KirghizAsia, avant le départ, de tout problème médical (allergie, antécédent médical, réaction à certains  médicaments, etc.), et de prendre, si besoin est, les médicaments qui vous sont spécifiques.
<br><b>Formalités :</b>
<br>Passeport valable au moins 6 mois après la date du retour + Visa (obtention possible à l'arrivée à l'aéroport de Bichkek).
Aucune vaccination obligatoire. Nous vous recommandons néanmoins d'être à jour dans vos rappels antitétaniques.
</p>
