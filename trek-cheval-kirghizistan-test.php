<p>
<br><b style="color: #E25A00;">Jour 1: Vol </b>
<br>Vol Kirghizistan 

<br><br><b style="color: #E25A00;">Jour 2: Bichkek. </b>
<br>Accueil à l’aéroport de Manas et transfert à Bichkek. Repos chez l’habitant. Visite de Bichkek, ville contrastée, à l’architecture soviétique et aux grands espaces verts. Vous visiterez le Grand Bazar Osh, ses allées animées, ses marchandises artisanales, ses contrastes culturels, entre spécialités chinoises et fruits secs du Moyen Orient. 
<br><i>Transfert : 45 min; Hébergement : Chez l'habitant</i>

<br><br><b style="color: #E25A00;">Jour 3: Randonnée Kizil-Sou </b>
<br>Très tôt dans la matinée transfert pour Kizil, alpage situé sur la chaîne nord de Tian Chan, d’où nous partirons pour une randonnée itinérante de 3 jours. Nous allons marcher le long de la chaîne nord de Tian Chan. La fraîcheur des glaciers des sommets à plus de 4400 m d’altitude se fait sentir. Les chevaux de bas portent des bagages et des nourritures.
<br><i>Heure de marche : 5/6 h ; Dénivelé : +800m ;  Hébergement : Tente </i>


<br><br><b style="color: #E25A00;">Jour 4: Randonnée jusqu’aux canyons de Boom</b>
<br>Dans la matinée une marche de quelques heures sur un plateau. Ensuite nous quittons les paysages de grands étendues et allons entrer dans la gorge de « Tchat » où les montagnes sont très arides. Avant d’arriver aux canyons de Boom nous traverserons plusieurs fois un petit torrent.  
<br><i>Heure de marche : 5/6 h ;  Dénivelé : +800m ;  Hébergement : Tente </i>

<br><br><b style="color: #E25A00;">Jour 5: Randonnée → Eshperov</b>
<br>Randonnée en remontant les canyons des Boom. Avant d’arriver à la route de Issyk-Kul, nous allons passer devant quelques anciens kolkhoz soviétiques.  Transfert à Eshperov, village situé au sud du lac Issyk-Kul.
<br><i>Transfert : 2 h ;  Heure de marche : 4 h ;  Dénivelé : +800m ;  Hébergement :  Chez l'habitant.</i>

<br><br><b style="color: #E25A00;">Jour 6: Les canyons d’Ak-Saï</b>
<br>Baignade et balade dans les canyons d’Ak-Saï.
<br><i>Heure de marche : 2 h ;  Hébergement : Chez l'habitant</i>

<br><br><b style="color: #E25A00;">Jours 7: Transfert à Song-Kol</b>
<br>Route pour le lac Song-Kol, situé au cœur des Monts Célestes à 3015 m d'altitude.  Après le passage du col Kalmak Achou à 3300 m d'altitude un  paysage époustouflant se présente à vous.
<br> <i> Transfert :  6 h ; Hébergement : Sous tente </i>

<br><br><b style="color: #E25A00;">Jour 8: Song-Kol </b>
<br>Journée libre à Song-Kol au bord du lac pour partager la vie des familles nomades. Possibilité de faire une balade à cheval pour les cavaliers débutants.
<br> <i>Hébergement : Sous la yourte</i>

<br><br><b style="color: #E25A00;">Jour 9: Randonnée Kizart </b>
<br>
Randonnée pour descendre au village de Kizart en passant par le col de Tuz-Achou.  Ensuite, route pour la vallée de Soussamir. Durant le trajet nous longerons la rivière de Kokomerene  qui coulent dans un splendide canyon aux couleurs ocres rouges. Nuit sous tente à 2700 m d’altitude.
<br> <i> Transfert :  3 h ;  Heure de marche : 4 h ; Dénivelé :+400m /-1000m ;  Hébergement : Tente</i> 

<br><br><b style="color: #E25A00;">Jour 10: Randonnée à cheval </b>
<br>Randonnée à cheval dans la vallée de Soussamir, un dès plus grands pâturages du Kirghizistan. 
Nous irons rendre visite aux nomades kirghiz installés avec leurs troupeaux de chevaux pour l’été. 
5-6 heures de randonnée, fin d’après-midi nous revenons à notre campement. 
<br> <i>Nuit sous tente à 2700 m d’altitude.</i>

<br><br><b style="color: #E25A00;">Jour 11: Randonnée à cheval</b>
<br>Randonnée à cheval dans la direction de jaïloo (pâturage en kirghiz) deTers. 
Pic-nique avant le col dans un endroit paisible entouré par de petits lacs. Après pic-nique nous allons franchir le col de Jaïsan 3600 m d’altitude. 
<br> <i> Nuit sous yourte chez les nomades kirghiz à 2600 m d’altitude.</i>

<br><br><b style="color: #E25A00;">Jour 12 : Randonnée à cheval</b>
<br>Randonnée à cheval, nous continuons notre randonnée itinérante. Ce jour là nous allons passer un autre col à 3400 m d’altitude. Pic-nique autour du lac glaciaire Boru Korboz. Après le passage du col nous redescendons dans la vallée de Kichi-Japyrtmakty. 
<br> <i> Nuit sous tente à 2700 m d’altitude. </i> 

<br><br><b style="color: #E25A00;">Jour 13: Randonnée à cheval </b>
<br>Randonnée à cheval, nous remontons sur les crêtes des montagnes Uzun-Kya. Arrivée en haut nous aurons la vue sur toute la région de Toktogoul, immense vallée avec un lac au milieu. 
<br> <i> Nuit sous tente à 1900 m d’altitude. </i> 

<br><br><b style="color: #E25A00;">Jour 14: Randonnée à cheval</b>
<br>Nous descendrons dans la vallée de Toktogoul. L’air frais de montagne reste la haut et la chaleur de la vallée se fait sentir dans les vallons sec et arides où auparavant les rois de Sythes ont été enterrés 3-2 siècle avant Jésus Christ. La maîtresse de la famille chez qui vous serez hébergés, est le professeur de français à l'école du village et le responsable d'une ONG qui développe l'artisanat traditionnel kirghiz. Occasion pour découvrir des tapis kirghiz artisanaux.
<br> <i>Nuit chez l’habitant.</i>

<br></br><b style="color: #E25A00;">Jour 15: Bichkek </b>
</br>Retour à Bishkek. Soirée d'adieu au restaurant traditionnel. 
</br> <i>Transfert :  4/5 h ;  Hébergement : Guesthouse </i>

<br></br><b style="color: #E25A00;">Jour 16: Vol de retour </b>
</br>Transfert à l'aéroport. 

</p>


<h2 style="text-align:center";>Fiche Technique </h2>
 
<p>
<b style="color: #E25A00;">Encadrement : </b>un guide kirghiz  francophone, une personne qui s’occupe des chevaux . Durant la randonnée les bagages sont portés par des chevaux.
<br><br><b style="color: #E25A00;">Déplacement sur place :</b> Minibus, voiture
<br><br><b style="color: #E25A00;">Climat : </b> Le climat au Kirghizstan est continental, avec un été chaud ( de 30 à 42°C) et un hiver froid (de -10 à -25°C). 
L'été, en altitude, la température varie entre +10 et +25°C le jour, et entre 0 et +5 °C la nuit. 
<br><br><b style="color: #E25A00;">Matériel nécessaire :</b>
 <br> - 1 petit sac à dos pour les affaires personnelles pour une journée
 <br> - 1 sac avec vos affaires de voyage (un seul par personne) qui nous suit lors de nos déplacements en minibus
 <br> - 1 duvet (confort -5°C) et 1 matelas gonflable.
 <br> - une tenue chaude pour les soirées en alpage, mais aussi une tenue légère (shorts et pantalons) pour les autres moments et une cape pour la pluie
 <br> - Chaussures de rechange, style tennis, ou sandales confortables
 <br> - Gourde de 1 litre
 <br>- Protection contre le soleil : chapeau, bob, lunettes, crèmes...
 <br> - Lampes électriques
 <br> - Affaires de toilette
 <br> - Pochette ventrale pour les papiers et autres effets personnels
 <br> - Pharmacie personnelle : protection contre les ampoules, petits problèmes intestinaux, désinfectant, antalgique. 
 <br><br><b style="color: #E25A00;">  Indispensable :</b> traitement de l'eau (micro pur), traitements personnels éventuels. 
   (L’accompagnateur dispose d’une pharmacie collective pour les soins d’urgence)
 <br><br><b style="color: #E25A00;">Remarque concernant les problèmes de santé et de pharmacie :</b>
Il est de la responsabilité de chacun d’entre vous d’avertir KirghizAsia, avant le départ, de tout problème médical (allergie, antécédent médical, réaction à certains  médicaments, etc.), et de prendre, si besoin est, les médicaments qui vous sont spécifiques.
<br><br><b style="color: #E25A00;">Formalités :</b>
<br>Passeport valable au moins 6 mois après la date du retour + Visa (obtention possible à l'arrivée à l'aéroport de Bichkek).
Aucune vaccination obligatoire. Nous vous recommandons néanmoins d'être à jour dans vos rappels antitétaniques.

</p>
