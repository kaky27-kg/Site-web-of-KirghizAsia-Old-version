 <br> - 1 sac avec vos affaires de voyage (un seul par personne) qui nous suit lors de nos déplacements en minibus
 <br> - 1 duvet (confort -5°C) et 1 matelas gonflable.
 <br> - Vêtements d'hiver et de ski
 <br> - Gourde de 1 litre
 <br> - Protection contre le soleil : chapeau, bob, lunettes, crèmes...
 <br> - Lampes électriques
 <br> - Affaires de toilette
 <br> - Pochette ventrale pour les papiers et autres effets personnels
 <br> - Pharmacie personnelle : protection contre les ampoules, petits problèmes intestinaux, désinfectant, antalgique. 
 <br><br><b style="color: #E25A00;">  Indispensable :</b> traitement de l'eau (micro pur), traitements personnels éventuels. 
   (L’accompagnateur dispose d’une pharmacie collective pour les soins d’urgence)
