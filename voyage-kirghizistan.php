			<p>
<b>Situation géographique</b>
<br>Le Kirghizstan se situe en Asie Centrale, encadré par la Chine à l’est et au Sud-est, le Kazakhstan au Nord, l’Ouzbékistan à l’ouest et 
le Tadjikistan au Sud-ouest. Il possède d’une superficie qui correspond à 198500 km², et il est totalement enclavé et ne possède pas d’accès à la mer. 
Les trois quarts du territoire sont occupés par des montagnes, majoritairement par le massif de Tian Shan et par le Pamir au Sud-ouest. Le massif 
de Tian Shan débute par le pic de Khan Tengri 7005m d’altitude (Roi du ciel) et se divise en trois parties : Tian Shan Nord, Tian Shan Centre et 
Tian Sud en culminant à 7439 au pic Jéniche (victoire en langue kirgize).
</p>
<img  class="img-responsive wow fadeIn" alt="carte_kirghizistan"  src="img/Carte_Kirghizistan.jpg">
<p>
</br>Les villes du Kirghizstan se situent dans les zones les plus basses du pays. Au Nord, la capitale Bichkek se trouve tout de même à 750 m d’altitude. 
Au Sud-ouest, l’autre foyer de peuplement dans la vallée du Fergana, on troue les villes d’Osh et de Jalal-Abad, ainsi que le point le plus bas du 
Kirghizstan, la vallée du Kara-Darya, à 132 m d’altitude. A l’est, le lac Issyk-Kul forme une petite mer intérieure de 6332 km² à 1620 m d'altitude,
 le deuxième plus grand lac de montagne du monde (après le lac Titicaca) dont le profondeur est 668 m. Le lac est légèrement salé et ne gèle pas en hiver.
Le climat au Kirghizstan est continental avec l’été chaud et sec, et l’hiver froid, et bien évidemment le climat montagneux a des influences aussi 
sur le temps. L’été, la température atteint parfois 42°C dans la capitale et dans la région du Sud. L’hiver dans certains lieux situés en altitude 
comme Naryn, Soussamyr la température baisse jusqu’à - 35°C la nuit. La saison de pluie est au printemps et en automne. L’hiver il neige, la hauteur
de la neige tombée par jour peut atteindre 30 cm. 
</p>
<p>
<b>Démographie</b>
<br>En 2004, la population du Kirghizistan était estimée à 5 081 429 habitants, 34,4% ayant moins de 15 ans et 6,2% plus de 65. Le pays est rural à 
66,1%, pour une densité de population assez faible de 29 habitants par km². 69,5% de la population est d'origine kirghize, un groupe ethnique turc 
présentant des influences mongoles et chinoises et étant historiquement constitué de bergers semi-nomades. La plus grande minorité est celle des Ouzbèkes 
(14,5% de la population), principalement situées dans le Sud, suivis par les personnes d'origine russe (9,0%, essentiellement dans le Nord), doungane (1,2%), 
ouïghour (1,1%), tadjike (1,1%), turque (0,9%), kazakh (0,7%), ukrainienne (0,5%) et coréenne (0,3%). Il existe également de petites communautés d'origine allemande. 
</p>
<p>
<b>Histoire</b> 
<br>D’après les sources chinoises, on entand parler des Kirghiz pour la première fois en 201 avant JC,  à cette époque-là dans le sud de la Sibérie autour du lac Baïkal 
(en langue turc-lac riche) jusqu’au milieu du fleuve Ienisseï (ce qui veut dire en turc vallée de mère) vivait le peuple « dinline » dont la culture était proche 
de celle des Sakas  (scythe) et des Gégounianes (appellation des anciens Kirghiz par les Chinois) dans l’Asie centrale. L’ouverture de la Route de la Soie entre la 
Chine et l’Occident durant le 2 siècle avant JC place l’Asie centrale au centre d’un équilibre entre grands empires. Toutes les perturbations politiques du 
Moyen-Orient, de la haute Asie et de la Chine font sentir leurs effets sur la fréquence et l’intensité des échanges, interrompus à certaines périodes. 
</p>
<p>
Le contrôle des voies d’accès vers l’ouest est une des raisons des luttes entre les dynasties chinoises et les confédérations des nomades. 
L’ancienne caravane séraï Tache Rabate  qui se trouve à 80 km de la frontière chinoise et qui servait de l’hôtel pour les commerçants de la Route de la Soie.
</p>
<p>
Par ailleurs, les Kirghiz sont turcophones, ce qui s’explique par la création du « khanat turc » en VI siècle qui était à l’origine des Huns dans l’Asie centrale. 
Entre 563 et 569, il détruit le royaume hephtalite de Transoxiane, région qu’il disputera à l’Empire sassanide jusqu’à la conquête arabo-islamique de la fin du 7 
siècle. La scission du khanat turc en deux parties vers 581 entraîne la formation du premier Etat turc d’Asie centrale occidentale, s’étendant du Kazakhstan au 
Tadjikistan. Les Turcs orientaux, battus par les Tang, réapparaissent vers 681 et s’unissent à nouveau aux Turcs occidentaux. Ces derniers sont repoussés par les 
Arabes tandis qu’en 744, la branche orientale est définitivement battue par les Chinois. Ceux-ci, à la poursuite des confédérations turques, se sont heurtés aux 
armées arabo-musulmanes sur la ville Talas. 
</p>
<p>
Vers XIII siècle, apparaît le royaume des Kirghiz dans l’Altaï et en Dzoungarie, certaines tribus ayant quitté leur territoire à la suite des invasions mongoles.  
Deux siècles plus tard, toutes les tribus rassemblées dans ce royaume vont prendre le nom d’ethnie kirghize. Donc, à partir de cette époque on peut dire que le peuple 
kirghiz est considéré comme nation. 
</p>
<p>
Avant d’être réunis à la Russie, au début du XIX° siècle, les Kirghiz sont sous la domination du royaume "Kokand". 
Ils se soulèveront plusieurs fois contre ce khanat, mais malheureusement, tous leurs efforts aboutiront à l’échec. 
Etant donné que les Kazakhs, ses voisins du Nord, vivaient mieux depuis qu’ils avaient rejoints à la Russie, les féodaux kirghiz envoient leurs ambassadeurs
en Russie pour demander leur rattachement à l’empire Russe. Les Russes acceptent volontiers leur proposition et viennent vers 1856 pour mettre en place leur pouvoir. 
Dans le nord du Kirghizstan, ils s’installent facilement et sans guerre, en revanche, dans le sud où le pouvoir du Khanat Kokand était encore fort, les Russes entrent 
en guerre avec eux et remportent la victoire. Ainsi les Russes s’installent dans la région et créent la République Turkestan, qui regroupe Sémiretchié, Syr-Daria, 
Fergana et Samarkand. En ce moment là, on comptait environ cinq cent mille Kirghiz.
</p>
<p>
La conquête et la domination coloniale russe provoquent assez peu de grandes révoltes jusqu’à 1916. Mais en 1916, une soudaine obligation faite aux musulmans de servir 
dans l’armée, dans des unités non combattantes sur le front européen déclanche la plus grande révolte. Plusieurs Kirghiz partent vers l’Afghanistan et la Chine, 
un grand nombre entre eux sont mort sur le chemin. 
</p>
<p>
En 1918, un soviet est fondé dans la région et l'oblast autonome Kara Kirghiz est créé en 1924 au sein de l'URSS. 
En 1926, il devient la République socialiste soviétique autonome de Kirghizie. En 1936, la République socialiste soviétique de Kirghizie est intégré comme membre à 
part entière de l'URSS. Lors des bouleversements politiques survenus à partir de la fin des années 1980 en Union Soviétique, des élections libres sont organisées au 
Kirghizistan en 1990 qui virent la victoire d'Askar Akaiev au poste de président en octobre de la même année. Le pays change de nom et devient la République Kirghize 
en décembre 1990, tandis que Frounzé, la capitale, est rebaptisé par son nom pré soviétique de Bichkek en février 1991.
</p>
<p>
<b>Culture</b>
<br>La culture kirghize se développe sur la base des traditions de l’Antiquité et du Moyen-Âge. Leur habitation – la yourte -  représente toute une culture kirghize 
avec sa construction, le matériel, la décoration intérieure,  les outils de l’homme et de la femme. Dessiné sur le drapeau kirghiz, le toit de la yourte 
est le symbole du peuple nomade. Toute la décoration intérieure de la yourte est faite en feutre.
</p>
<p>
La nourriture kirghize est esentiellement à la base de la viande et du produit laitier. La boissont traditionnelle des Kirghiz et de tous les peuples nomades de l’Asie centrale 
est le "Koumiz".Du lait de jument fermentée dans un sac en cuire et mélangé avec un bâton en bois qui s’appelle Bichkek. Notamment ce nom a été donné à la capitale du 
Kirghizstan. 
</p>
<p>
Ancien calendrier kirghiz est établi en fonction des périodes de chasse et les mois portent les noms des animaux.
Par ailleurs, chez  les kirghiz l’écriture apparaît tardivement. Plus tard, avec l’arrivé des Arabes au XIII° siècle les Kirghiz 
