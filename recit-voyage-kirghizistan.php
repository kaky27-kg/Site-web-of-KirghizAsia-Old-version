<p>	

<b>Jean-Pierre Aubert </b>: jean-pier.aubert@orange.fr		
<br>Retours et commentaires sur notre trek au KIRGHIZSTAN en 2012

<br><br>Nos commentaires sur ce beau voyage :
<br><br>Écrit par Catherine GL et complété par les commentaires de Damien, Jocelyne et Jean-Pierre
<br><br>Circuit : bien compte tenu des distances entre les différents sites de rando.
<br><br>D'où l'importance de discuter entre ce que l'on souhaite et ce qui est possible.

<br><br>Organisation, transfert, logistique : plutôt bien avec les ressources du pays. Une des maisons
d'hôte à Echperov pas très bien même si confortable tout de même.
Le camion de Nicolai était parfait pour les transferts que nous avons faits.
L organisation des courses était laborieuse visiblement! Ils devraient prendre l habitude de faire une
liste en fonction des menus avant de courir dans tous les sens. C'est peut être ce qu'ils faisaient ?
<br><br>Le point positif : on s'est baladé dans plein de marches et c était super car c'est là qu'il y a la vie des
villages. J'ai aimé ces moments. Et c'est l'avis général.
Une meilleure préparation des possibilités de repas avec les achats correspondant nous paraît
possible en écrivant tout cela sur papier. Certains manquants auraient pu être évités .
<br><br>Les visites : fabrique de yourtes, églises, mosquées marchés...étaient peu nombreuses mais assez
bien réparties sur le trajet ce qui a contribué à équilibrer le voyage.
Au niveau des paysages , le Jaïloo du SARALASAZ et les lacs du SONG KOL et ISSYK KOL
ont été les plus dépaysants et beaux dans leur immensité. La partie Karakol ressemblait plus à nos
ALPES et était également très belle et notamment la vallée du Téléty et le lac ALA KOL qui est
vraiment magique.

<br><br>Les guides :
Talant est parfait, à l'écoute des gens et attentif, plein d humour, beaucoup de connaissances. Il
faudrait qu'il s'améliore un peu à être Le Guide de tout le groupe et diplomatiquement à savoir être
le chef Question d âge peut être et puis il y avait beaucoup d indépendants a forte personnalité dans
notre groupe ! certains d entre nous on du l impressionner aussi avec leur expérience.
Dansolook il a tout à apprendre et surtout le français puis à s'affermir pour prendre quelques
initiatives.
<br><br>Alexandre : sur deux jours il a été très bien et connaissant mieux cette zone que Talant sur le
parcours en tant que guide, plus expérimenté je pense à guider un groupe et comme ils étaient deux
c'était aussi + simple pour lui.
<br><br>Nicolai : un chauffeur sympa et plein d'humour lui aussi attentif aux personnes du groupe.
Très professionnel dans la conduite et les habitudes du pays et prévenants sur les envies et besoins
de tous. Son âge et son expérience est très complémentaire avec la jeunesse et dynamisme de
Talant.
<br><br>Les cavaliers : les deux premiers bien, les 2 jeunes de Kizart à virer ! Odieux dont l un qui se
prenait pour un autre ( fils du vétérinaire de la maison d'hôtes ?) et particulièrement mal élevé (ce n
est pas de la pudeur comme à voulu l expliquer Talant)
Les jeunes porteurs très sympas et courageux pour gagner de l'argent l'été. Bien en phase avec
Alexandre pour le timing et l'organisation. Ceux qui parlaient anglais ont permis aussi quelques
échanges.
Nos hôtes dans les yourtes ont été chaleureux, ceux des chambres d'hôtes tout en étant avenants
nous ont parus plus distant. La langue a certainement été un obstacle à l'échange.

<br><br>La nourriture : Globalement correct mais un peu juste parfois pour certains d'entre nous je pense
et aussi pas toujours adaptée aux besoins vus les efforts physiques. Et spécialement un manque
de protéines dans les grandes étapes. Mais aussi de très bons repas le soir au campement avec les
moyens du bord. Pour les habitués qui cherchent entrée, plat, fromage, dessert il faut s'adapter aux
habitudes locales et revoir leurs critères de découverte d un pays.

<br><br>Talant pourrait aussi donner quelques règles de distribution compte tenu des quantité disponibles.
En conclusion un petit effort sur le contenu et la quantité dans les moments cruciaux serait un petit
plus souhaité.

<br><br>La sécurité : les chemins ne présentaient pas de difficultés et donc pas de pb de sécurité. Par contre,
j'imagine qu'en cas d'accident cela devient compliquer comme partout dans un pays qui n est pas la
Haute Savoie ! Il y avait toujours des cavaliers qui surgissaient de nul part, cela peut aider....
Nous nous sommes toujours sentis en sécurité et l'endroit le plus dangereux me paraît être
« la vallée de la mort » la route entre le lac ISSYK KOL et BISHKEK à cause des travaux et
des camions et des Chinois. Mais nous avions confiance à NICOLAI et personne n'a exprimer de
craintes.

<br><br>Conclusion:
Il faut prendre ces retours et commentaires comme des éléments de réflexion positive pour
améliorer les suivant et dire honnêtement le sentiment de chacun.
L'organisation de KIRGHIZASIA a été une réussite puisque le programme, les dates, l'esprit du
trek ont été respectés malgré les petits soucis d'avion, de pluie etc...Petits soucis réglés au fur et à
mesure avec TALANT et KALYINUR que nous remercions pour leur gentillesse leurs compétences
et leurs dévouements pour leurs clients.
</p>
