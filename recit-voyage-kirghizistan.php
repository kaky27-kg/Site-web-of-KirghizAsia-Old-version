<p>
<b>Bruno JEANDIDIER</b> E-mail:Bruno.Jeandidier@univ-nancy2.fr
<br><br>Quant au voyage de cet été, autant te dire tout de go que personnellement j'ai été ravi de ce mois en Asie Centrale, et donc je n'ai vraiment pas grande critique à faire :
<br>- les paysages sont splendides, le melange entre chaines alpines et prairies mongoles est depaysant, la randonnée est agréable car il n'y a pas grand monde sur les sentiers et car la meteo ete super (temps variable, donc pas trop chaud, avec finalement peu de pluie, juste un peu d'orage le soir), les baignades sont fraiches mais stimulantes, la durée des étapes est correcte, les kirghises sont hyper accueillants, mais il est dommage qu'ils ne parlent pas un peu anglais (on pourrait a l'inverse de nous repprocher de ne pas parler russe), la confiture et les fruits sont si bons qu'ont prend des kilos facilement, l'hebergement en yourte ou en maison d'hote est vraiment un plus pour etre mieux en phase avec le pays, les "restes" du sovietisme constituent un element culturel interessant pour un europeen, la tolerance religieuse est rassurante...
<br>- le programme était bien equilibré entre randonnée et jours de transit
<br>- porteurs, chevaux, bus, tentes, etc... rien à redire
<br>- j'ai particulierement apprécié de manger local (meme si c'était au prix d'avoir du mounton chaque jour, j'ai en effet horreur de manger international lorsque je visite un pays authentique)
<br>- les deux guides etait tres professionnels malgré leur jeune age (sourriants, parlant bien le francais, prevenants, bon marcheurs, bon cuisinier, sympathiques, causants, ayant de l'humour...), bref ce sont des perles, c'est pas toujours le cas dans d'autres pays.

<br>Seuls points à améliorer de mon point de vue :
<br>- il faut absolument des réchauds performants (de maniere à ne pas diner trop tard) ;
<br>- une tente mess pour le repas du soir lorsqu'il n'y a pas de yourte serait plus agreable lorsque la temperature est basse.

<br>En tout cas, personnellement j'ai apprecié l'esprit "petite agence" et donc je t'encourage a poursuivre ton entreprise (autant que possible sans devenir un monstre - par la taille - de tourisme).

Ici, je dis à tous ceux qui m'interroge sur ton pays que "c'est sûr, j'y retournerai".

</p>
