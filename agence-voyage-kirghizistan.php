		<h2>Formation des accompagnateurs en montagne</h2>
<p>Chaque année nous mettons en place une formation au métier de guide accompagnateur avant le commencement de la saison touristique.
La formation vise à donner des compétences pratiques et théoriques aux guides accompagnateurs. Le programme de formation comprend les disciplines suivantes :
<br>-La cartographie
<br>-L’orientation
<br>-L’itinéraire
<br>-La conduite de groupe
<br>-La sécurité 
<br>-L’alimentation
<br>-La faune et la flore
<br>-La culture générale
<br>-Les connaissances touristiques
<br>-La pratique de la langue française


<br><br>Pour certaines disciplines, il est prévu une introduction théorique. Pour les autres, la pratique se fait avec les sorties sur place, en visitant des itinéraires possibles et en apprenant aux futurs guides accompagnateurs la conduite de groupe dans des conditions réelles. 
<br><br>La particularité de notre association paraît bien évidente pour nos étudiants, car nous formons non seulement pour délivrer des diplômes, mais ce qui est essentiel, nous les formons pour qu’ils puissent travailler en cherchant à les placer au sein de différentes agences touristique, ce que nous arrivons à faire avec succès. 

<br><br> <b>2011: Première formation de guide-accompagnateur - Avec la participation de Pierre-Joseph Copier</b>
<br><br><img class="img-responsive wow fadeIn" src="img/pierre-josephe.jpg" />
<br><br>En 2011 nous avons organisé la première formation de guide-accompagnateur avec l’aide de Pierre-Joseph Copier, guide de montagne chevronné basé à Montmaur en Diois dans la Drôme (26), et spécialiste de marche nordique. La formation a d'abord débuté par une formation théorique de plusieurs semaines, et s'est poursuivie par des sorties sur le terrain pour mettre en pratique les enseignements appris au préalable.
<br><br> <b>2012: Seconde formation de guide-accompagnateur - Avec la participation de Philippe Guyot</b>
<br><br>Pour la formation 2012, nous avons décidé de garder les mêmes bases de formation, en travaillant toujours sur les aspects théorique et pratique de la formation au métier de guide accompagnateur. Cette fois-ci nous avons travaillé avec Philippe Guyot, un vrai passionné du voyage, fort d'une expérience d'organisateur et d'encadrant de voyage à travers la planète. Il est lui aussi basé dans la Drôme, dans la localité de Die.
<br><br><img class="img-responsive wow fadeIn"  src="img/philippe guyot.jpg" />
<br><br> <b>2013: Troisième formation de guide-accompagnateur - Avec la participation de Talant Kirecheev et Florian Barboteau</b>
<br><br><img src="images/formation/randonnée_guide_kirghizstan.png" />
<br><br>Pour cette édition 2013, ce sont deux dynamiques passionnés de trekking qui se sont occupés de la formation de guide-accompagnateur. Face au succès de cette initiative, puisque 12 jeunes Kirghizes ont participé à cette édition 2013, deux encadrants étaient bien nécessaire pour prodiguer le meilleur enseignement possible.
<br>Talant est un guide de montagne déjà doté d'une très grande expérience pour son âge. Originaire de la région de Toktogoul, il parcourt sans cesse depuis de nombreuses années déjà les Monts Tien-Shan. Afin de parfaire son français, et de s'expérimenter à la pratique du ski de randonnée, il a passé les hivers 2011 et 2012 dans les Alpes, entre Vercors et Chartreuse.
<br>Florian, également originaire de la Drôme, a décidé de rejoindre l'équipe de guide-accompagnateur pour cette saison 2013, tout en poursuivant ses études en master de tourisme à l'université d'Angers. Il était notamment chargé d'évaluer le niveau de français des prétendants, leur culture générale, mais aussi leurs connaissances dans le domaine touristique.
<br><br>KirghizAsia reste ouverte à toute collaboration avec différentes organisations et compagnies kirghizes et/ou étrangères, ainsi qu’avec des guides professionnels locaux et/ou français. 

<p>
Pour nous l'importance ce n'est pas la quantité de voyages à organiser, c'est la qualité de chaque voyage et les echanges entre chaque voyageur et la population locale. 

Au Kirghizstan, tant de choses à découvrir au niveau culture, histoire et enfin la nature, mais aussi tant de choses à apprendre pour nous à travers vous les voyageurs !!! 

