<p>
<br><b style="color: #E25A00;">Jour 1: Vol pour Kirghizistan</b>

<br><br><b style="color: #E25A00;">Jour 2: Bichkek</b>
<br>Arrivée tôt le matin à l’aéroport de Manas. Transfert à Bichkek et repos chez l’habitant. 
Visite de Bichkek, ville contrastée, à l’architecture soviétique et aux grands espaces verts. 
Vous visiterez le Grand Bazar Osh, ses allées animées, ses marchandises artisanales, ses mélanges culturels, 
entre spécialités chinoises et fruits secs du Moyen Orient. 
<br><i>Nuit chez l’habitant.</i>

<br><br><b style="color: #E25A00;">Jour 3 : Vallée de Soussamir</b>
<br>Route pour la vallée de Soussamir 3 heures de route. L’Après-midi, nous retrouvons nos chevaux. 
<br><i>Nuit sous tente à 2700 m d’altitude.</i>

<br><br><b style="color: #E25A00;">Jour 4 : Randonnée à cheval</b>
<br>Randonnée à cheval dans la vallée de Soussamir, un dès plus grands pâturages du Kirghizistan. Nous irons rendre visite aux nomades kirghiz installés avec leurs troupeaux de chevaux pour l’été. 5-6 heures de randonnée, fin d’après-midi nous revenons à notre campement. 
<br><i>Nuit sous tente à 2700 m d’altitude.</i>

<br><br><b style="color: #E25A00;">Jour 5 : Randonnée à cheval </b>
<br>Randonnée à cheval dans la direction de jaïloo (pâturage en kirghiz) deTers. Pic-nique avant le col dans un endroit paisible entouré par de petits lacs. Après pic-nique nous allons franchir le col de Jaïsan 3600 m d’altitude. 
<br><i>Nuit sous yourte chez les nomades kirghiz à 2600 m d’altitude.</i>

<br><br><b style="color: #E25A00;">Jour 6 : Randonnée à cheval</b>
<br>Randonnée à cheval, nous continuons notre randonnée itinérante. Ce jour là nous allons passer un autre col à 3400 m d’altitude. Pic-nique autour du lac glaciaire Boru Korboz. Après le passage du col nous redescendons dans la vallée de Kichi-Japyrtmakty. 
<br><i>Nuit sous tente à 2700 m d’altitude. </i>

<br><br><b style="color: #E25A00;">Jour 7 : Randonnée à cheval</b>
<br>Randonnée à cheval, nous remontons sur les crêtes des montagnes Uzun-Kya. Arrivée en haut nous aurons la vue sur toute la région de Toktogoul, immense vallée avec un lac au milieu. 
<br><i>Nuit sous tente à 1900 m d’altitude. </i>

<br><br><b style="color: #E25A00;">Jour 8 : Randonnée à cheval</b>
<br>Nous descendrons dans la vallée de Toktogoul. L’air frais de montagne reste la haut et la chaleur de la vallée se fait sentir dans les vallons sec et arides où auparavant les rois de Sythes ont été enterrés 3-2 siècle avant Jésus Christe. 
La maîtresse de la famille chez qui vous serez hébergés, est le professeur de français à l'école du village et le responsable d'une ONG qui développe l'artisanat traditionnel kirghiz. Occasion pour découvrir des tapis kirghiz artisanaux. 
<br><i>Nuit chez l’habitant.</i>

<br><br><b style="color: #E25A00;">Jour 9: Randonnée à cheval</b>
<br>Balade à cheval vers le lac de Toktogoul en traversant des villages et des champs de culture. Possiblité de se baigner au lac. 
<br><i>Nuit chez l'habitant.</i>

<br><br><b style="color: #E25A00;">Jour 10: Kizart</b>
<br>Route pour Kizart, village situé au pied des motagnes de Song-Kol 5 heures de route. 
<br><i>Nuit chez l'habitant.</i>

<br><br><b style="color: #E25A00;">Jour 11: Randonnée à cheval </b>
<br>Nous allons monter au lac Son-Kol, 6-7 heures de rando en passant par la vallée de Kilemché. Arrivée au col de "Jalguiz Karagaï", un paysage époustouflant se présente à vous. 
<br><i>Nuit sous yourte.</i>

<br><br><b style="color: #E25A00;">Jour 12: Randonnée à cheval</b>
<br>Balade à cheval autour du lac Son-Kol à 3015 m d'altitude. Vous allez découvrir la vie nomade du peuple.
<br><i>Nuit sous yourte. </i>

<br><br><b style="color: #E25A00;">Jour 13: Randonnée à cheval</b>
<br>On redescend au village de Kizart en empruntant un autre chemain.  Après-midi nous prenons la route vers le lac Issik-Kul. 
<br><i>Nuit sous tente au bord du lac.</i>

<br><br><b style="color: #E25A00;">Jour 14: Issyk-Kul</b>
<br>Repos au bord du lac Issyk-Kul, un endroit paisible entouré par des canyons rouges et une vue magnifique sur les sommets enneigés des montagnes. Soirée autour du feu. 
<br><i>Nuit sous tente.</i>

<br><br><b style="color: #E25A00;">Jour 15: Bichkek</b>
<br>Retour à Bichkek. 5 heures de route. Dîner d’adieu au restaurant. 
<br><i>Nuit chez l'habitant.</i>

<br><br><b style="color: #E25A00;">Jour 16: Vol de retour</b>
<br>Transfert à l'aéroport. Vol. 

